package z_6;

public class Student {
    int IDnumer;
    String name;

    public Student() {
    }


    public String toString() {
        return "IDnumer=" + IDnumer +", name=" + name ;
    }

    public void setIDnumer(int IDnumer) {
        this.IDnumer = IDnumer;
    }

    public void setName(String name) {
        this.name = name;
    }
}
